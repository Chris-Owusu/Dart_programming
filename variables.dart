void main() {
  // variables


  // strings
  String name = "Jong";
  print("My name is " + name);

  // Integers
  var x = 23;
  print(x);

  // Dynamic variables
  dynamic firstName = "Matthew";
  print(firstName + ": This is a dynamic variable");


  // const and final
  const String fullName = "John Elder";   // used at compiled time
  final age = 34;    // used at runtime. eg the time always change

  print("$fullName is $age years old.");




}